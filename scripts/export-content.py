THIN = Side(style="thin", color=BRASS_DEEP)
ALL_BORDERS = Border(left=THIN, right=THIN, top=THIN, bottom=THIN)


def style_header_cell(cell):
    cell.font = Font(name="Calibri", size=11, bold=True, color=INK_DEEP)
    cell.fill = PatternFill("solid", fgColor=BRASS)
    cell.alignment = Alignment(horizontal="right", vertical="center", wrap_text=True)
    cell.border = ALL_BORDERS


def style_title_cell(cell):
    cell.font = Font(name="Calibri", size=14, bold=True, color=IVORY)
    cell.fill = PatternFill("solid", fgColor=COCOA)
    cell.alignment = Alignment(horizontal="right", vertical="center", wrap_text=True)
    cell.border = ALL_BORDERS


def style_body_cell(cell, zebra=False):
    cell.font = Font(name="Calibri", size=10, color=INK_DEEP)
    if zebra:
        cell.fill = PatternFill("solid", fgColor=MIST)
    cell.alignment = Alignment(horizontal="right", vertical="top", wrap_text=True)
    cell.border = ALL_BORDERS


def write_table(ws, start_row, headers, rows, col_widths=None):
    """Writes a header row + data rows. Returns the next available row."""
    # headers
    for ci, h in enumerate(headers, start=1):
        cell = ws.cell(row=start_row, column=ci, value=h)
        style_header_cell(cell)
    ws.row_dimensions[start_row].height = 28

    # data
    for ri, row in enumerate(rows, start=start_row + 1):
        zebra = (ri - start_row) % 2 == 0
        for ci, val in enumerate(row, start=1):
            cell = ws.cell(row=ri, column=ci, value=val)
            style_body_cell(cell, zebra=zebra)

    # column widths
    if col_widths:
        for ci, w in enumerate(col_widths, start=1):
            ws.column_dimensions[get_column_letter(ci)].width = w

    ws.freeze_panes = ws.cell(row=start_row + 1, column=1)
    return start_row + len(rows) + 2


def write_section_title(ws, row, title, span):
    ws.cell(row=row, column=1, value=title)
    ws.merge_cells(start_row=row, start_column=1, end_row=row, end_column=span)
    cell = ws.cell(row=row, column=1)
    style_title_cell(cell)
    ws.row_dimensions[row].height = 32
    return row + 1


