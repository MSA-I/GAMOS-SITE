def _set_para_rtl(paragraph):
    pPr = paragraph._p.get_or_add_pPr()
    bidi = OxmlElement("w:bidi")
    bidi.set(qn("w:val"), "1")
    pPr.append(bidi)


def _set_run_rtl(run):
    rPr = run._element.get_or_add_rPr()
    rtl = OxmlElement("w:rtl")
    rtl.set(qn("w:val"), "1")
    rPr.append(rtl)


def _set_cell_shading(cell, fill_hex):
    tcPr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), fill_hex)
    tcPr.append(shd)


def _add_bottom_border(paragraph, color_hex, size="6"):
    pPr = paragraph._p.get_or_add_pPr()
    pBdr = OxmlElement("w:pBdr")
    bottom = OxmlElement("w:bottom")
    bottom.set(qn("w:val"), "single")
    bottom.set(qn("w:sz"), size)
    bottom.set(qn("w:space"), "1")
    bottom.set(qn("w:color"), color_hex)
    pBdr.append(bottom)
    pPr.append(pBdr)


def add_h(doc, text, level, color_hex=None, size_pt=None, bottom_border=False):
    p = doc.add_paragraph()
    p.style = doc.styles[f"Heading {level}"] if level <= 9 else doc.styles["Heading 9"]
    p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    _set_para_rtl(p)
    run = p.add_run(text)
    _set_run_rtl(run)
    if size_pt:
        run.font.size = Pt(size_pt)
    if color_hex:
        run.font.color.rgb = RGBColor.from_string(color_hex)
    run.font.bold = True
    if bottom_border:
        _add_bottom_border(p, BRASS, size="8")
    return p


def add_para(doc, text, color_hex=None, size_pt=11, italic=False, indent_in=0,
             space_after=6):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    _set_para_rtl(p)
    p.paragraph_format.space_after = Pt(space_after)
    p.paragraph_format.line_spacing = 1.4
    if indent_in:
        p.paragraph_format.right_indent = Inches(indent_in)
    run = p.add_run(text)
    _set_run_rtl(run)
    run.font.size = Pt(size_pt)
    if italic:
        run.font.italic = True
    if color_hex:
        run.font.color.rgb = RGBColor.from_string(color_hex)
    return p


def add_eyebrow(doc, text):
    """Small uppercase brass-deep label above a heading."""
    if not text or text == "—":
        return
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    _set_para_rtl(p)
    p.paragraph_format.space_after = Pt(0)
    run = p.add_run(text.upper())
    _set_run_rtl(run)
    run.font.size = Pt(9)
    run.font.bold = True
    run.font.color.rgb = RGBColor.from_string(BRASS_DEEP)


def add_bullets(doc, items):
    for item in items:
        p = doc.add_paragraph(style="List Bullet")
        p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
        _set_para_rtl(p)
        run = p.add_run(item)
        _set_run_rtl(run)
        run.font.size = Pt(11)
        run.font.color.rgb = RGBColor.from_string(INK_DEEP)


def add_kv_table(doc, rows, col_widths=(2.0, 4.5)):
    """rows = list of (label, value)"""
    table = doc.add_table(rows=len(rows), cols=2)
    table.alignment = WD_TABLE_ALIGNMENT.RIGHT
    table.autofit = False
    for i, (label, value) in enumerate(rows):
        for j, txt in enumerate((label, value)):
            cell = table.rows[i].cells[j]
            cell.width = Inches(col_widths[j])
            _set_cell_shading(cell, MIST if i % 2 == 0 else "FFFFFF")
            cell.vertical_alignment = WD_ALIGN_VERTICAL.TOP
            cell_p = cell.paragraphs[0]
            cell_p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
            _set_para_rtl(cell_p)
            run = cell_p.add_run(str(txt))
            _set_run_rtl(run)
            run.font.size = Pt(10)
            if j == 0:
                run.font.bold = True
                run.font.color.rgb = RGBColor.from_string(BRASS_DEEP)
            else:
                run.font.color.rgb = RGBColor.from_string(INK_DEEP)
    add_para(doc, "", space_after=6)


def add_data_table(doc, headers, rows, col_widths=None):
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.alignment = WD_TABLE_ALIGNMENT.RIGHT
    table.autofit = False
    # header row
    for j, h in enumerate(headers):
        cell = table.rows[0].cells[j]
        if col_widths:
            cell.width = Inches(col_widths[j])
        _set_cell_shading(cell, BRASS)
        cp = cell.paragraphs[0]
        cp.alignment = WD_ALIGN_PARAGRAPH.RIGHT
        _set_para_rtl(cp)
        run = cp.add_run(h)
        _set_run_rtl(run)
        run.font.size = Pt(10)
        run.font.bold = True
        run.font.color.rgb = RGBColor.from_string(INK_DEEP)
    # data rows
    for i, row in enumerate(rows, start=1):
        zebra = i % 2 == 0
        for j, val in enumerate(row):
            cell = table.rows[i].cells[j]
            if col_widths:
                cell.width = Inches(col_widths[j])
            _set_cell_shading(cell, MIST if zebra else "FFFFFF")
            cell.vertical_alignment = WD_ALIGN_VERTICAL.TOP
            cp = cell.paragraphs[0]
            cp.alignment = WD_ALIGN_PARAGRAPH.RIGHT
            _set_para_rtl(cp)
            run = cp.add_run(str(val))
