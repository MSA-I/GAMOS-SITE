def configure_default_style(doc):
    """Set the default Normal style to a Hebrew-friendly font, RTL-aware."""
    style = doc.styles["Normal"]
    style.font.name = "David"
    style.font.size = Pt(11)
    # eastAsia / cs / ascii bindings
    rpr = style.element.get_or_add_rPr()
    rFonts = rpr.find(qn("w:rFonts"))
    if rFonts is None:
        rFonts = OxmlElement("w:rFonts")
        rpr.append(rFonts)
    rFonts.set(qn("w:cs"), "David")
    rFonts.set(qn("w:hAnsi"), "Calibri")
    rFonts.set(qn("w:ascii"), "Calibri")


def configure_section_rtl(doc):
    """Make every section right-to-left."""
    for section in doc.sections:
        sectPr = section._sectPr
        bidi = OxmlElement("w:bidi")
        sectPr.append(bidi)
        section.right_margin = Cm(2.0)
        section.left_margin = Cm(2.0)
        section.top_margin = Cm(2.0)
        section.bottom_margin = Cm(2.0)


def add_toc(doc):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    _set_para_rtl(p)
    run = p.add_run()
    _set_run_rtl(run)
    fldChar1 = OxmlElement("w:fldChar")
    fldChar1.set(qn("w:fldCharType"), "begin")
    instrText = OxmlElement("w:instrText")
    instrText.set(qn("xml:space"), "preserve")
    instrText.text = r'TOC \o "1-3" \h \z \u'
    fldChar2 = OxmlElement("w:fldChar")
    fldChar2.set(qn("w:fldCharType"), "separate")
    fldChar3 = OxmlElement("w:t")
    fldChar3.text = "תוכן עניינים — לחיצה ימנית → Update Field כדי לעדכן"
    fldChar4 = OxmlElement("w:fldChar")
    fldChar4.set(qn("w:fldCharType"), "end")
    run._r.append(fldChar1)
    run._r.append(instrText)
    run._r.append(fldChar2)
    run._r.append(fldChar3)
    run._r.append(fldChar4)


def add_footer(doc):
    for section in doc.sections:
        footer = section.footer
        p = footer.paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        _set_para_rtl(p)
        run = p.add_run("© 2026 גאמוס · gamos.co.il · מסמך פנים-אדמיניסטרטיבי")
        _set_run_rtl(run)
        run.font.size = Pt(9)
        run.font.color.rgb = RGBColor.from_string(COCOA)


